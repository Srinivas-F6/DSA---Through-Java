public class Theory {
    public static void main(String[] args) {
        // Collection: The root interface in the hierarchy. It defines basic methods
        //              for working with groups of objects.

        // List: An ordered collection (also known as a sequence). Elements can be accessed by their integer index.
        // Implementations:
        //          ArrayList
        //          LinkedList
        //          Vector
        //           Stack

        // Set: A collection that does not allow duplicate elements. It models the mathematical set abstraction.
        //       Implementations:
        //                HashSet
        //                LinkedHashSet
        //                TreeSet

        // Queue: A collection designed for holding elements prior to processing. Typicall y,
        //        it orders elements in a FIFO (first-in-first-out) manner.
        //          Implementations:
        //                 PriorityQueue
        //                 LinkedList (also implements Queue)

        // Map: An object that maps keys to values, with no duplicate keys allowed. Each key can map to at most one value.
        //            Implementations:
        //                     HashMap
        //                     LinkedHashMap
        //                     TreeMap
        //                     Hashtable
  
        // Common Operations
        //         Adding Elements: Use methods like add(), put(), or offer().
        //         Removing Elements: Use methods like remove(), clear().
        //         Iterating: Use loops or iterators to traverse through collections.
        //         Sorting: Use Collections.sort() for lists.

        //      ArrayList: Fast for random access but slow for inserts/deletes (except at the end).
        //     LinkedList: Fast for inserts/deletes but slow for random access.
        //     HashSet/HashMap: Provide average O(1) time complexity for add, remove, and contains operations.
    }
}
